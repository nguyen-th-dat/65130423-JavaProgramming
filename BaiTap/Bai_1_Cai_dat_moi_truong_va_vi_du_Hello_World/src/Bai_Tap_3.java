public class Bai_Tap_3 {
    public static void main(String[] args) {
        // phep tinh 1 + 1
        System.out.println("1 + 1 = " + (1 + 1));
        System.out.println("");

        // phep tinh 10 - 5
        System.out.println("10 - 5 = " + (10 - 5));
        System.out.println("");

        // Bang cuu chuong 2
        System.out.println("Bang cuu chuong 2");
        for (int i = 1; i <= 10; i++){
            System.out.println("2 x " + i +" = " + (2 * i));

        }
        System.out.println("");

        // phep tinh 100 / 3
        System.out.println("100 / 3 = " + (100.0 / 3));
    }

}
